# lista com valores constantes

# adicionar a vírgula no final de uma tupla, mesmo quando ela tem apenas um elemento, 
#é uma boa prática para evitar confusões e 
#garantir que o código seja interpretado corretamente. 

#exemplos de como delcarar uma tupla
tupla = (1,2,3,)
tupla1 = tuple('python') # tuplas com strings
tupla2 = tuple([7,8,9]) # tuplas com números 
tupla3 = ('Brasil',)

print(tupla)
print(tupla1)
print(tupla2)
print(tupla3)

print(type(tupla3))